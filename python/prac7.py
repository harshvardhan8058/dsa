import turtle

# create a turtle object
t = turtle.Turtle()
t.speed(10)

# set the background color
screen = turtle.Screen()
screen.bgcolor("#87CEEB")

# draw the roof of the house
t.pencolor("#8B4513")
t.fillcolor("#8B4513")
t.begin_fill()
t.forward(150)
t.left(120)
t.forward(150)
t.left(120)
t.forward(150)
t.left(120)
t.end_fill()

# draw the main part of the house
t.pencolor("#8B0000")
t.fillcolor("#FFDAB9")
t.begin_fill()
t.left(30)
t.forward(150)
t.right(90)
t.forward(150)
t.right(90)
t.forward(150)
t.right(90)
t.forward(150)
t.right(60)
t.forward(150)
t.right(120)
t.forward(150)
t.right(60)
t.end_fill()

# draw the door
t.penup()
t.goto(20, -150)
t.pendown()
t.pencolor("#8B0000")
t.fillcolor("#FFDAB9")
t.begin_fill()
t.forward(60)
t.left(90)
t.forward(100)
t.left(90)
t.forward(60)
t.left(90)
t.forward(100)
t.end_fill()

# draw the windows
t.penup()
t.goto(-100, -50)
t.pendown()
t.pencolor("#8B0000")
t.fillcolor("#ADD8E6")
t.begin_fill()
t.left(45)
t.forward(60)
t.right(90)
t.forward(60)
t.right(90)
t.forward(60)
t.right(90)
t.forward(60)
t.right(45)
t.end_fill()

t.penup()
t.goto(80, -50)
t.pendown()
t.begin_fill()
t.left(45)
t.forward(60)
t.right(90)
t.forward(60)
t.right(90)
t.forward(60)
t.right(90)
t.forward(60)
t.right(45)
t.end_fill()

# draw the chimney
t.penup()
t.goto(-50, 50)
t.pendown()
t.pencolor("#8B0000")
t.fillcolor("#FFDAB9")
t.begin_fill()
t.left(90)
t.forward(40)
t.right(90)
t.forward(20)
t.right(90)
t.forward(40)
t.right(90)
t.forward(20)
t.end_fill()

# hide the turtle cursor
t.hideturtle()

# keep the window open until it's closed
turtle.done()
